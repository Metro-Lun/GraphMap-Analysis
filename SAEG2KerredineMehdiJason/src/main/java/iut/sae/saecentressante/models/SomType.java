package iut.sae.saecentressante.models;



/**
 * Enumeration des differents types possible pour un sommet.
 * 
 * @author Mehdi BOURBON
 * @version 1.1
 * @see Graphe
 * @see Graphe.Sommet#type
 */
public enum SomType {
    
    
    
    /**
     * Represente une maternite.
     */
    MATER,
    
    /**
     * Represente un bloc operatoire.
     */
    OPE,
    
    /**
     * Represente un centre nutritionnel.
     */
    NUTRI;
    
}
